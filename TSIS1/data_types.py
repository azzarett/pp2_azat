#1, int
x = 5
print(type(x))

#2, str
x = "Hello World"
print(type(x))

#3, float
x = 20.5
print(type(x))

#4, list
x = ["apple", "banana", "cherry"]
print(type(x))

#5, tuple
x = ("apple", "banana", "cherry")
print(type(x))

#6, dict
x = {"name" : "John", "age" : 36}
print(type(x))

#7, bool
x = True
print(type(x))


